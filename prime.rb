def prime?(num)
   
    
    if  num == 3 || num == 5 || num == 5
        return true
    else
        if num % 2 == 0 ||  num % 3 == 0 || num % 5 == 0 || num % 7 == 0 || num % 9 == 0 || num == 1 
            return false
        else
            return true
        end
    end
    
    
end